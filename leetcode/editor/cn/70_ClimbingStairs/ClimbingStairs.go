func climbStairs(n int) int {
  dp:=make([]int,n+1)
  for i:=1;i <= n;i++{
  	if  i <= 2 {
  		dp[i] = i
	}else{
		dp[i] = dp[i-1] + dp[i-2]
	}
  }
  return dp[n]
}
