func lenLongestFibSubseq(arr []int) int {
	length := len(arr)
	// arrä¸­å€¼åˆ° indexçš„æ˜ å°„
	index := make(map[int]int,length)
	for i,v :=range arr{
		index[v] = i
	}
	//x,y => dp[x,y]çš„æ˜ å°„,è¿™é‡Œä¸ºäº†å¯ä»¥æ ¹æ®(x,y)ä½œä¸ºmapçš„key,æˆ‘ä»¬å°† ï¼ˆx,yï¼‰è½¬æ¢æˆx*length+y
	dpArr := make(map[int]int,length)
	maxLength := 0
	// arr[i] + arr[j]  = arr [k]
	// æˆ‘ä»¬è¦æ±‚æ‰€æœ‰çš„dp[j,k]ï¼Œé‚£ä¹ˆæˆ‘ä»¬å…ˆè¦å»dp[i,j] ; ç„¶ådp[j,k] =dp[i,j]+1
	// j,kå¯ä»¥æ˜¯æ•°ç»„ä¸­çš„ä»»æ„ä¸¤ä¸ª

	for  j :=0;j < length-1;j++{
		for k := j+1;k <length;k++ {
			//æ±‚a[i]çš„ä¸­içš„å€¼
			num :=  arr[k] - arr[j]
			// åˆ©ç”¨mpå¿«é€ŸæŸ¥æ‰¾arr[i]çš„ä¸‹æ ‡
			i :=-1
			v,ok := index[num]
			if ok {
				i = v
			}
			if i >= 0 && i < j {
				dp,ok := dpArr[i*length+j]
				// æœ€å°çš„dp[i,j]
				if !ok {
					dp= 2
				}
				// æ ¹æ®dp[i,j]å»æ±‚dp[j,k]
				key :=  j*length+k
				dpArr[key] = dp + 1
				if dpArr[key] >= 3 {
					maxLength = max( dpArr[key], maxLength)
				}
			}
		}
	}
	return maxLength
}

