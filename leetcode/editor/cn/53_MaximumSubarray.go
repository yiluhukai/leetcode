func maxSubArray(nums []int) int {
	accum,max := 0,nums[0]

	for _,val :=range nums {
		accum = maxVal(accum+val,val)
		max = maxVal(accum,max)
	}
	return max
}

func maxVal(a,b int)int{
	if a > b{
		return a
	}
	return b
}
